def test_benchmark_with_external_header(client, monkeypatch):
    """Mocked detector returns a result; the row is included."""

    class _FakeDetector:
        URL = "https://example.test/api"

        def detect(self, text, *, timeout=8.0):
            return DetectorResult(score=0.78, band="high", confidence=0.92)

    monkeypatch.setattr(
        "sis_caro_humanizer.serve.routes.benchmark.get_detector",
        lambda name: _FakeDetector(),
    )
    r = client.post(
        "/v1/benchmark",
        json={"text": "x" * 100, "detectors": ["gptzero"]},
        headers={**HDRS, "X-External-Benchmark": "yes"},
    )
    assert r.status_code == 200
    rows = r.json()["external"]
    assert rows and rows[0]["detector"] == "gptzero"
    assert rows[0]["score"] == 0.78


def test_benchmark_detector_failure_does_not_abort(client, monkeypatch):
    """One detector raising DetectorUnavailable still returns 200 and a row
    with `error` for the failing detector and no abort of the others."""

    from sis_caro_humanizer.scoring.external import DetectorUnavailable

    class _BadDetector:
        URL = "https://example.test/api"

        def detect(self, text, *, timeout=8.0):
            raise DetectorUnavailable("rate_limited")

    class _GoodDetector:
        URL = "https://example.test/api2"

        def detect(self, text, *, timeout=8.0):
            return DetectorResult(score=0.4, band="medium", confidence=None)

    def picker(name):
        if name == "gptzero":
            return _BadDetector()
        return _GoodDetector()

    monkeypatch.setattr("sis_caro_humanizer.serve.routes.benchmark.get_detector", picker)
    r = client.post(
        "/v1/benchmark",
        json={"text": "x" * 100, "detectors": ["gptzero", "sapling"]},
        headers={**HDRS, "X-External-Benchmark": "yes"},
    )
    assert r.status_code == 200
    rows = r.json()["external"]
    assert len(rows) == 2
    assert rows[0]["error"] == "rate_limited"
    assert "score" in rows[1]


