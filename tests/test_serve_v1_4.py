def test_llm_run_unavailable_backend_returns_502(client, monkeypatch):
    """When the chosen backend is unconfigured, route should 502."""

    class FakeBackend:
        def is_available(self):
            return False

        def rewrite(self, *a, **kw):
            raise NotImplementedError

    monkeypatch.setattr(
        "sis_caro_humanizer.serve.routes.benchmark.get_backend",
        lambda name: FakeBackend(),
    )
    r = client.post(
        "/v1/llm/run",
        json={"prompt": "hi", "backend": "anthropic"},
        headers=HDRS,
    )
    assert r.status_code == 502
    assert r.json()["error"] == "backend_unavailable"


def test_llm_run_happy_path(client, monkeypatch):
    class FakeBackend:
        def is_available(self):
            return True

        def rewrite(self, prompt, *, system, model=None, timeout=120.0):
            return f"RESPONSE: {prompt}"

    monkeypatch.setattr(
        "sis_caro_humanizer.serve.routes.benchmark.get_backend",
        lambda name: FakeBackend(),
    )
    r = client.post(
        "/v1/llm/run",
        json={"prompt": "hello there", "backend": "ollama"},
        headers=HDRS,
    )
    assert r.status_code == 200
    body = r.json()
    assert body["output"].startswith("RESPONSE:")
    assert isinstance(body["elapsed_seconds"], float)


